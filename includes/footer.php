      window.addEventListener('scroll', function() {
        if (window.scrollY > 50) {
          navbar.classList.add('scrolled');
        } else {
          navbar.classList.remove('scrolled');
        }
      });
      
      // Active nav link highlighting
      const currentPath = window.location.pathname;
      const navLinks = document.querySelectorAll('.nav-link');
      
      navLinks.forEach(link => {
        const href = link.getAttribute('href');
        if (currentPath.includes(href) && href !== '/final/index.php' && href !== '#') {
          link.classList.add('active');
          
          // If this is a dropdown item, also highlight parent dropdown
          const parentDropdown = link.closest('.dropdown');
          if (parentDropdown) {
            parentDropdown.querySelector('.dropdown-toggle').classList.add('active');
          }
                } else if (currentPath === '/' || currentPath === '/index.php') {
          document.querySelector('a[href="/final/dashboard.php"]').classList.add('active');
        }
      });
    });
